# =============================================================================
# Phase 1: 欠損値、異常値、重複イベントのクリーニング
#
# 【クリーニング方針】
#
# Step 1: Alarm Event End のみに絞る
#   - 説明書の通り、アラーム履歴として有効なのは "Alarm Event End" 行のみ
#   - Alarms Silenced / Low Battery 等のシステムイベント行は除外
#   - 除外行は別途 outputs/system_events_non_alarm.csv に保存済み（01_load_data.R）
#
# Step 2: 重複除去 → 不要と判明
#   - alarm_start（分精度）で重複チェックすると25,220件ヒットしたが、
#     datetime（秒精度）で確認すると真の重複は0件
#   - 同一分内に同一ベッドで同種アラームが複数発生した正当な別イベントだった
#   → 除外なし
#
# Step 3: duration_sec = 0 のフラグ付与
#   - 0秒アラームが28,643件（約15%）存在する
#   - 記録精度上1秒未満で終了したと解釈し、除外はしない
#   - duration_zero フラグを付与して解析時に区別できるようにする
#
# Step 4: 長時間アラーム（>3600秒 = 1時間超）のフラグ付与
#   - 1,163件（約0.6%）が1時間超
#   - 医療者が未対応のまま継続した正当なアラームの可能性あり
#   - 除外はしないが duration_long フラグを付与する
#
# =============================================================================

library(readr)
library(dplyr)

# -----------------------------------------------------------------------------
# 0. 読み込み
# -----------------------------------------------------------------------------

df <- readRDS("data/proceeded/01_loaded.rds")
n_before <- nrow(df)
cat("クリーニング前行数:", n_before, "\n\n")

# -----------------------------------------------------------------------------
# Step 1: Alarm Event End のみに絞る
# -----------------------------------------------------------------------------

removed_step1 <- df |> filter(項目 != "Alarm Event End")
df <- df |> filter(項目 == "Alarm Event End")

cat("[Step 1] Alarm Event End に絞る\n")
cat("  除外行数:", nrow(removed_step1), "\n")
cat("  残り行数:", nrow(df), "\n\n")

# -----------------------------------------------------------------------------
# Step 2: 重複確認（秒精度datetimeで確認 → 真の重複なし）
# -----------------------------------------------------------------------------

n_true_dup <- df |>
  group_by(ベッド名, datetime, 内容１, 優先度, duration_sec) |>
  filter(n() > 1) |>
  nrow()

cat("[Step 2] 真の重複確認（datetime秒精度）\n")
cat("  真の重複行数:", n_true_dup, "→ 除外なし\n\n")

# -----------------------------------------------------------------------------
# Step 3 & 4: フラグ付与
# -----------------------------------------------------------------------------

df <- df |>
  mutate(
    duration_zero = duration_sec == 0,          # 0秒アラームフラグ
    duration_long = duration_sec > 3600         # 1時間超アラームフラグ
  )

cat("[Step 3] duration_zero フラグ（0秒アラーム）\n")
cat("  件数:", sum(df$duration_zero), "(", round(mean(df$duration_zero)*100, 1), "%)\n\n")

cat("[Step 4] duration_long フラグ（1時間超アラーム）\n")
cat("  件数:", sum(df$duration_long), "(", round(mean(df$duration_long)*100, 1), "%)\n\n")

# -----------------------------------------------------------------------------
# クリーニングログの保存
# -----------------------------------------------------------------------------

log <- tibble(
  step        = c("Step1_非アラームイベント除外", "Step2_真の重複（除外なし）",
                  "Step3_duration_zeroフラグ",   "Step4_duration_longフラグ"),
   対象行数   = c(nrow(removed_step1), n_true_dup,
                  sum(df$duration_zero), sum(df$duration_long)),
  処置       = c("除外", "除外なし（真の重複0件）", "フラグ付与のみ", "フラグ付与のみ"),
  クリーニング後行数 = c(nrow(df), nrow(df), nrow(df), nrow(df))
)

write_excel_csv(log, "outputs/cleaning_log.csv")
cat("クリーニングログ保存: outputs/cleaning_log.csv\n\n")

# -----------------------------------------------------------------------------
# 保存
# -----------------------------------------------------------------------------

saveRDS(df, "data/proceeded/02_cleaned.rds")
cat("保存完了: data/proceeded/02_cleaned.rds\n")
cat("クリーニング前:", n_before, "行 → クリーニング後:", nrow(df), "行\n")
